function render(time: number) {
  time *= 0.001; // convert time to seconds

  cubes.forEach((cube, ndx) => {
    const speed = 1 + ndx * 0.1;
    const rot = time * speed;
    cube.rotation.x = rot;
    cube.rotation.y = rot;
  });
  renderer.render(scene, camera);

  requestAnimationFrame(render);
}
requestAnimationFrame(render);
